export default (state = initialState, action) => {
  switch (action.type) {
    case GET_DAYPLAN:
      localStorage.setItem(`planFor.TripId.${action.payload.tripId}`, JSON.stringify(action.payload.planner));
      return {
        ...state,
        ...action.payload.planner,
      };
    case HAS_DAYPLAN:
      localStorage.getItem(`planFor.TripId.${action.payload.tripId}`);
      return {
        ...state,
        ...action.payload.planner,
      };

    default:
      return state;
  }
};
